    private void cargarArchivo() {
        File directorio = new File("src/partidasGuardada");

        if (!directorio.exists() || !directorio.isDirectory()) {
            System.out.println("El directorio no existe o no es vÃ¡lido: " + directorio.getAbsolutePath());
            return;
        }

        File[] archivosTxt = directorio.listFiles((dir, name) -> name.toLowerCase().endsWith(".txt"));

        comboCargarParida.removeAllItems();

        if (archivosTxt != null && archivosTxt.length > 0) {
            for (File archivo : archivosTxt) {
                comboCargarParida.addItem(archivo.getName());  
            }
            comboCargarParida.setSelectedIndex(0); 
        } else {
            comboCargarParida.addItem("No hay partidas guardadas");
            System.out.println("No se encontraron archivos .txt en el directorio.");
        }
    }

